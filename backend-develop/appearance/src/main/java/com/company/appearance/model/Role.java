package com.company.appearance.model;

/**
 * Enumeration representing user roles in the system.
 * Defines the available roles for user accounts.
 */
public enum Role {
    /**
     * Administrator role with full system access.
     */
    ADMIN,
    
    /**
     * Evaluator role with limited access for evaluation operations.
     */
    EVALUATOR
}
